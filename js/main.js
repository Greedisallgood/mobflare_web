function applyI18n(lang){
  const dict = I18N[lang] || I18N.en;
  document.documentElement.lang = lang;
  const rtl = lang === 'he' || lang === 'ar';
  document.documentElement.dir = rtl ? 'rtl' : 'ltr';
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key = el.getAttribute('data-i18n');
    const value = key.split('.').reduce((o,k)=> (o||{})[k], dict);
    if(Array.isArray(value)){
      if(el.tagName === 'UL'){
        el.innerHTML='';
        value.forEach(v=>{ const li=document.createElement('li'); li.textContent=v; el.appendChild(li); });
      }
    } else if(typeof value === 'string'){
      if(el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') el.placeholder = value; else el.textContent = value;
    }
  });
  const fb = document.querySelector('.footer-bottom p');
  if(fb && dict.footer?.copyright){ fb.textContent = dict.footer.copyright; }
}

function initLangSwitcher(){
  const currentBtn = document.getElementById('lang-current');
  const list = document.getElementById('lang-list');
  if(!currentBtn || !list){ return; }
  const saved = localStorage.getItem('lang') || 'en';
  currentBtn.textContent = saved === 'en' ? 'English' : saved==='ru' ? 'Русский' : saved==='he' ? 'עברית' : 'العربية';
  applyI18n(saved);
  currentBtn.addEventListener('click', ()=>{
    const open = list.hasAttribute('hidden') === false;
    if(open){ list.setAttribute('hidden',''); currentBtn.setAttribute('aria-expanded','false'); }
    else { list.removeAttribute('hidden'); currentBtn.setAttribute('aria-expanded','true'); }
  });
  list.addEventListener('click', (e)=>{
    const li = e.target.closest('li[data-lang]');
    if(!li) return;
    const lang = li.getAttribute('data-lang');
    localStorage.setItem('lang', lang);
    currentBtn.textContent = lang === 'en' ? 'English' : lang==='ru' ? 'Русский' : lang==='he' ? 'עברית' : 'العربية';
    list.setAttribute('hidden',''); currentBtn.setAttribute('aria-expanded','false');
    applyI18n(lang);
  });
  document.addEventListener('click', (e)=>{ if(!document.getElementById('lang-switch').contains(e.target)){ list.setAttribute('hidden',''); currentBtn.setAttribute('aria-expanded','false'); }});
}

// Smooth scrolling navigation
document.addEventListener('DOMContentLoaded', function() {
    // Initialize language switcher & i18n first
    initLangSwitcher();
    // Mobile menu toggle
    const hamburger = document.querySelector('.hamburger');
    const navMenu = document.querySelector('.nav-menu');

    hamburger.addEventListener('click', function() {
        hamburger.classList.toggle('active');
        navMenu.classList.toggle('active');
    });

    // Close mobile menu when clicking on a link
    document.querySelectorAll('.nav-link').forEach(link => {
        link.addEventListener('click', () => {
            hamburger.classList.remove('active');
            navMenu.classList.remove('active');
        });
    });

    // Smooth scrolling for navigation links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                const headerHeight = document.querySelector('.header').offsetHeight;
                const targetPosition = target.offsetTop - headerHeight;
                
                window.scrollTo({
                    top: targetPosition,
                    behavior: 'smooth'
                });
            }
        });
    });

    // Header background change on scroll
    const header = document.querySelector('.header');
    window.addEventListener('scroll', function() {
        if (window.scrollY > 100) {
            header.style.background = 'rgba(255, 255, 255, 0.98)';
            header.style.boxShadow = '0 2px 20px rgba(0, 0, 0, 0.1)';
        } else {
            header.style.background = 'rgba(255, 255, 255, 0.95)';
            header.style.boxShadow = 'none';
        }
    });

    // Scroll animations
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver(function(entries) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, observerOptions);

    // Add animation classes to elements
    const animatedElements = document.querySelectorAll('.service-card, .case-study-card, .testimonial-card, .contact-item, .section-header');
    animatedElements.forEach((el, index) => {
        el.classList.add('fade-in');
        el.style.animationDelay = `${index * 0.1}s`;
        observer.observe(el);
    });

    // Add slide animations to hero content
    const heroContent = document.querySelector('.hero-content');
    const heroVisual = document.querySelector('.hero-visual');
    
    if (heroContent) {
        heroContent.classList.add('slide-in-left');
        observer.observe(heroContent);
    }
    
    if (heroVisual) {
        heroVisual.classList.add('slide-in-right');
        observer.observe(heroVisual);
    }

    // Floating cards animation
    const floatingCards = document.querySelectorAll('.floating-card');
    floatingCards.forEach((card, index) => {
        card.style.animationDelay = `${index * 0.5}s`;
    });

    // Parallax effect for hero section
    window.addEventListener('scroll', function() {
        const scrolled = window.pageYOffset;
        const hero = document.querySelector('.hero');
        if (hero) {
            const rate = scrolled * -0.5;
            hero.style.transform = `translateY(${rate}px)`;
        }
    });

    // Counter animation for metrics
    const animateCounters = () => {
        const counters = document.querySelectorAll('.metric-number');
        counters.forEach(counter => {
            const target = parseInt(counter.textContent.replace(/[^\d]/g, ''));
            const increment = target / 100;
            let current = 0;
            
            const updateCounter = () => {
                if (current < target) {
                    current += increment;
                    if (counter.textContent.includes('K+')) {
                        counter.textContent = Math.ceil(current / 1000) + 'K+';
                    } else if (counter.textContent.includes('M+')) {
                        counter.textContent = Math.ceil(current / 1000000) + 'M+';
                    } else if (counter.textContent.includes('$')) {
                        counter.textContent = '$' + current.toFixed(2);
                    } else if (counter.textContent.includes('%')) {
                        counter.textContent = Math.ceil(current) + '%';
                    } else {
                        counter.textContent = Math.ceil(current);
                    }
                    requestAnimationFrame(updateCounter);
                } else {
                    if (counter.textContent.includes('K+')) {
                        counter.textContent = target / 1000 + 'K+';
                    } else if (counter.textContent.includes('M+')) {
                        counter.textContent = target / 1000000 + 'M+';
                    } else if (counter.textContent.includes('$')) {
                        counter.textContent = '$' + target.toFixed(2);
                    } else if (counter.textContent.includes('%')) {
                        counter.textContent = target + '%';
                    } else {
                        counter.textContent = target;
                    }
                }
            };
            
            updateCounter();
        });
    };

    // Trigger counter animation when case studies section is visible
    const caseStudiesSection = document.querySelector('.case-studies');
    if (caseStudiesSection) {
        const caseStudiesObserver = new IntersectionObserver(function(entries) {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    animateCounters();
                    caseStudiesObserver.unobserve(entry.target);
                }
            });
        }, { threshold: 0.5 });
        
        caseStudiesObserver.observe(caseStudiesSection);
    }

    // Add hover effects to service cards
    const serviceCards = document.querySelectorAll('.service-card');
    serviceCards.forEach(card => {
        card.addEventListener('mouseenter', function() {
            this.style.transform = 'translateY(-10px) scale(1.02)';
        });
        
        card.addEventListener('mouseleave', function() {
            this.style.transform = 'translateY(0) scale(1)';
        });
    });

    // Add typing effect to hero title
    const heroTitle = document.querySelector('.hero-title');
    if (heroTitle) {
        const text = heroTitle.textContent;
        heroTitle.textContent = '';
        let i = 0;
        
        const typeWriter = () => {
            if (i < text.length) {
                heroTitle.textContent += text.charAt(i);
                i++;
                setTimeout(typeWriter, 100);
            }
        };
        
        setTimeout(typeWriter, 500);
    }

    // Add scroll progress indicator
    const createScrollProgress = () => {
        const progressBar = document.createElement('div');
        progressBar.style.cssText = `
            position: fixed;
            top: 0;
            left: 0;
            width: 0%;
            height: 3px;
            background: linear-gradient(90deg, #80ED20, #6BCF00);
            z-index: 9999;
            transition: width 0.1s ease;
        `;
        document.body.appendChild(progressBar);
        
        window.addEventListener('scroll', () => {
            const scrollTop = window.pageYOffset;
            const docHeight = document.body.scrollHeight - window.innerHeight;
            const scrollPercent = (scrollTop / docHeight) * 100;
            progressBar.style.width = scrollPercent + '%';
        });
    };
    
    createScrollProgress();
});
